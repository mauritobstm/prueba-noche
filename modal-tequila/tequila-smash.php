                                    <li>Agrega el jarabe de agave o miel y usa un mortero para machacar suavemente las hojas de menta y el pepino para liberar sus sabores.</li>
                                    <li>Añade el tequila y el jugo de limón fresco al vaso.</li>
                                    <li>Agrega hielo al vaso y remueve suavemente para mezclar los ingredientes.</li>
                                    <li>(opcional) Decora con una rodaja de limón y unas hojas de menta.</li>
