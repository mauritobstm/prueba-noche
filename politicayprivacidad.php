<h1 class="subheading">Política de Privacidad para Nochecaribeña.com</h1>

<h2 class="subheading">Última actualización: 01/02/2024</h2>

<h4 class="subheading">Gracias por visitar Nochecaribeña.com.</h4>
<p style="font-size: 18px;">En esta página, describimos cómo recopilamos, usamos y protegemos la información personal que pueda proporcionarnos mientras utiliza nuestro sitio web.</p>

<h3>Información que recopilamos</h3>
<h3>Información Personalmente Identificable</h3>
<p>Cuando utiliza nuestro sitio web, podemos recopilar cierta información personalmente identificable, como su nombre, dirección de correo electrónico, y otra información que pueda proporcionar voluntariamente al registrarse o completar formularios en nuestro sitio.</p>

<h2>Información de Registro</h2>
<p>Recopilamos información que su navegador envía cada vez que visita nuestro sitio web ("Datos de Registro"). Estos Datos de Registro pueden incluir información como la dirección IP de su computadora, tipo de navegador, versión del navegador, las páginas de nuestro sitio que visita, la hora y la fecha de su visita, el tiempo que pasa en esas páginas y otros datos estadísticos.</p>

<h2>Cookies</h2>
<p>Las cookies son archivos con una pequeña cantidad de datos que se almacenan comúnmente como un identificador anónimo. Estas se envían a su navegador desde el sitio web que visita y se almacenan en el disco duro de su computadora.</p>

<p>Utilizamos "cookies" para recopilar información y mejorar nuestro servicio. Puede indicar a su navegador que rechace todas las cookies o que le avise cuando se envía una cookie. Sin embargo, si no acepta cookies, es posible que no pueda utilizar algunas partes de nuestro sitio web.</p>

<h2>Uso de la Información</h2>
<h3>Utilizamos la información recopilada para diversos fines, que incluyen:</h3>

<p>Proporcionar, mantener y mejorar nuestro sitio web.</p>
<p>Notificarle sobre cambios en nuestro sitio web.</p>
<p>Permitirle participar en funciones interactivas de nuestro sitio web cuando elija hacerlo.</p>
<p>Proporcionar soporte al cliente.</p>
<p>Realizar análisis o recopilar información valiosa para mejorar nuestro sitio.</p>
<p>Compartir Información</p>
<p>No vendemos, intercambiamos ni transferimos de ninguna manera su información personal a terceros sin su consentimiento, excepto según lo permitido por las leyes aplicables.</p>

<h2>Enlaces a Otros Sitios Web</h2>
<p>Nuestro sitio web puede contener enlaces a otros sitios que no son operados por nosotros. Si hace clic en un enlace de terceros, será dirigido al sitio de ese tercero. Le recomendamos encarecidamente que revise la Política de Privacidad de cada sitio que visite.</p>

<h2>Cambios a esta Política de Privacidad</h2>
<p>Podemos actualizar nuestra Política de Privacidad de vez en cuando. Le notificaremos de cualquier cambio publicando la nueva Política de Privacidad en esta página. Se le aconseja revisar esta Política de Privacidad periódicamente para cualquier cambio. Los cambios a esta Política de Privacidad son efectivos cuando se publican en esta página.</p>

